
class bcolors:
    """
    Class def for getting colorful output on terminal.
    """

    PINK = '\033[95m'
    BLUE = '\033[94m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    RED = '\033[91m'

    WARNING = '\033[93m'  # this is a bright yellow

    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'

    RESET = '\033[0m'
